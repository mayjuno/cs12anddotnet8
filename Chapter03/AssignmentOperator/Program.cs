namespace AssignmentOperator
{
    internal class Program
    {
        static void Main(string[] args)
        {

            int p = 6;
            p += 3; // Equivalent to: p = p + 3;
            p -= 3; // Equivalent to: p = p - 3;
            p *= 3; // Equivalent to: p = p * 3;
            p /= 3; // Equivalent to: p = p / 3;
            Console.WriteLine(p);
        }
    }
}
